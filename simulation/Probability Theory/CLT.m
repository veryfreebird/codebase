% 加载statistics包以使用normpdf函数
pkg load statistics

% 设置x轴范围，这里我们取从-10到10的范围，适用于展示不同的正态分布
x = linspace(-10, 10, 1000);

% 定义均值和一组不同的标准差
mu = 0;  % 所有分布的均值相同
sigmas = [0.5, 1, 2, 3, 4];  % 不同的标准差

% 创建一个新的图形窗口
figure;

% 对于每组标准差，计算并绘制对应的正态分布曲线
hold on;  % 允许在同一个图上绘制多条曲线
colors = lines(length(sigmas));  % 为不同的曲线分配不同的颜色
for i = 1:length(sigmas)
    sigma = sigmas(i);
    y = normpdf(x, mu, sigma);  % 计算概率密度值
    plot(x, y, 'LineWidth', 2, 'Color', colors(i, :));  % 绘制曲线
end

% 添加标题、坐标轴标签和图例
title('不同标准差下的正态分布曲线');
xlabel('值');
ylabel('概率密度');
legend(arrayfun(@(sigma) sprintf('σ = %.1f', sigma), sigmas, 'UniformOutput', false));
grid on;  % 显示网格线
hold off;  % 关闭保持状态

%{
标准差的含义
离散度：标准差衡量了一组数值与其平均值（均值，μ）之间的偏离程度。较小的标准差意味着大多数数值集中在平均值附近；较大的标准差则表示这些数值更分散地分布在平均值周围。
  68-95-99.7规则（经验法则）：对于一个正态分布的数据集：
    大约68%的数据落在距离平均值一个标准差的范围内（即μ±σ）。
    大约95%的数据落在距离平均值两个标准差的范围内（即μ±2σ）。
    大约99.7%的数据落在距离平均值三个标准差的范围内（即μ±3σ）。
形状影响：标准差直接影响正态分布曲线的宽度和高度。标准差越大，曲线越宽且越低；标准差越小，曲线越窄且越高。这是因为标准差决定了数据点围绕平均值的集中程度。
数据分布的理解：在实际应用中，标准差帮助我们理解数据的波动情况。例如，在质量控制中，了解生产过程中产品尺寸的标准差可以帮助确定产品的可接受误差范围。
